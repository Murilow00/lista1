let num1 = 3
let num2 = 8
let conta = '+'
switch(conta){
    case '+':
        console.log(num1+num2)
            break
    case '-':
        console.log(num1-num2)
            break
    case '*':
        console.log(num1*num2)
            break
    case '/':
        console.log(num1/num2)
            break
    default:
        console.log('insira um operador valido')
}
